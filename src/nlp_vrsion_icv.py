        if not isinstance(df, pd.DataFrame) or df.empty:
            logger.warning("⚠️ DataFrame vazio, nenhum registro processado.")
            return

        for _, row in tqdm(df.iterrows(), total=len(df), desc="derm7pt"):
            case_num = int(row["case_num"])
            diagnostico_raw = str(row.get("diagnosis", "desconhecido")).strip()
            diagnostico_ptbr = self.DIAGNOSTICOS_DERM7PT.get(
                diagnostico_raw.lower(), diagnostico_raw
            )

