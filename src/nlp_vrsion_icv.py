    def carregar_modelos(self):
        """Carrega todos os modelos (chamar uma vez)."""
        import gc

        # 1. spaCy
        logger.info("📦 [1/4] Carregando spaCy (pt_core_news_sm)...")
        self.nlp = spacy.load("pt_core_news_sm")
        # Aumentar limite para textos longos
        self.nlp.max_length = 3_000_000

        # 2. BERTimbau (para embeddings de tokens)
        logger.info(f"📦 [2/4] Carregando BERTimbau ({Config.BERT_MODEL})...")
        from transformers import AutoTokenizer, AutoModel
        self.tokenizer = AutoTokenizer.from_pretrained(Config.BERT_MODEL)
        self.bert_model = AutoModel.from_pretrained(Config.BERT_MODEL).to(DEVICE)
        self.bert_model.eval()

        # 3. Zero-shot classifier
        logger.info(f"📦 [3/4] Carregando classificador zero-shot ({Config.ZERO_SHOT_MODEL})...")
        from transformers import pipeline
        self.classifier = pipeline(
            "zero-shot-classification",
            model=Config.ZERO_SHOT_MODEL,
            device=0 if DEVICE == "cuda" else -1
        )

        # 4. Sentence-BERT
        logger.info(f"📦 [4/4] Carregando Sentence-BERT ({Config.SENTENCE_MODEL})...")
        from sentence_transformers import SentenceTransformer
        self.sentence_model = SentenceTransformer(Config.SENTENCE_MODEL, device=DEVICE)

        # Pré-computar embeddings das sementes médicas + mapeamento semente→categoria
        logger.info("🌱 Computando embeddings das sementes médicas...")
        self._sementes_embeddings = self.sentence_model.encode(
            self.SEMENTES_MEDICAS, show_progress_bar=False
        )
        self._sementes_categorias = []
        cat_ranges = [
            ("DOENÇA", 0, 49),          # Doenças e condições
            ("NEOPLASIA", 49, 66),       # Neoplasias
            ("INFECÇÃO", 66, 82),        # Infecções e patógenos
            ("LESÃO_ELEMENTAR", 82, 108),# Lesões elementares
            ("SINTOMA", 108, 119),       # Sintomas
            ("ESTRUTURA", 119, 134),     # Estruturas da pele
            ("MORFOLOGIA", 134, 152),    # Descritores morfológicos
            ("EXAME", 152, 163),         # Exames e diagnóstico
            ("PROCEDIMENTO", 163, 179),  # Procedimentos
            ("MEDICAMENTO", 179, 200),   # Medicamentos
        ]
        for cat, start, end in cat_ranges:
            for _ in range(start, min(end, len(self.SEMENTES_MEDICAS))):
                self._sementes_categorias.append(cat)
        # Preencher restantes se houver
        while len(self._sementes_categorias) < len(self.SEMENTES_MEDICAS):
            self._sementes_categorias.append("OUTRO")

        logger.info(f"   {len(self.SEMENTES_MEDICAS)} sementes mapeadas para "
                     f"{len(set(self._sementes_categorias))} categorias")

        gc.collect()
        if DEVICE == "cuda":
            torch.cuda.empty_cache()

