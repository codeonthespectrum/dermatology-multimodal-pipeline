class ClinicalVariableProcessor:
    """
    Processa variáveis clínicas conforme especificação da professora:
    - age: numérico, ausentes → mediana, normalizado via Z-score
    - sex: male/female/unknown → 0/1/2
    - localization: texto → inteiros iterativos (0, 1, 2...)

    IMPORTANTE: Esta classe gera os valores crus E os normalizados.
    A normalização Z-score da idade é feita depois, quando todos os registros
    estão disponíveis (precisa da mediana/std global).
    """

    SEX_MAP = {"male": 0, "female": 1, "unknown": 2}

    def __init__(self):
        self.loc_encoder = LabelEncoder()
        self.idades_coletadas = []
        self.localizacoes_coletadas = []
        self._fitted = False

    def coletar(self, idade, sexo, localizacao):
        """Coleta valores para fit posterior."""
        if idade is not None and not pd.isna(idade):
            try:
                self.idades_coletadas.append(float(idade))
            except (ValueError, TypeError):
                pass
        if localizacao and str(localizacao).strip():
            self.localizacoes_coletadas.append(str(localizacao).strip().lower())

    def fit(self):
        """Calcula mediana de idade e encaixa encoder de localização."""
        self.mediana_idade = np.median(self.idades_coletadas) if self.idades_coletadas else 50.0
        self.std_idade = np.std(self.idades_coletadas) if self.idades_coletadas else 15.0
        if self.std_idade == 0:
            self.std_idade = 1.0

        # Fit localização
        locs_unicas = sorted(set(self.localizacoes_coletadas))
        if locs_unicas:
            self.loc_encoder.fit(locs_unicas)
        self._fitted = True

        logger.info(f"📊 Variáveis clínicas - Idade mediana: {self.mediana_idade:.1f}, "
                     f"Localizações únicas: {len(locs_unicas)}")

    def processar_idade(self, idade_raw) -> Tuple[float, float]:
        """Retorna (idade_original, idade_zscore)."""
        if idade_raw is None or pd.isna(idade_raw):
            idade = self.mediana_idade
        else:
            try:
                idade = float(idade_raw)
            except (ValueError, TypeError):
                idade = self.mediana_idade
        zscore = (idade - self.mediana_idade) / self.std_idade
        return idade, zscore

    def processar_sexo(self, sexo_raw) -> Tuple[str, int]:
        """Retorna (sexo_texto, sexo_codigo)."""
        if sexo_raw is None or pd.isna(sexo_raw):
            sexo = "unknown"
        else:
            sexo = str(sexo_raw).strip().lower()
        if sexo not in self.SEX_MAP:
            sexo = "unknown"
        return sexo, self.SEX_MAP[sexo]

    def processar_localizacao(self, loc_raw) -> Tuple[str, int]:
        """Retorna (localizacao_texto, localizacao_codigo)."""
        if loc_raw is None or pd.isna(loc_raw) or str(loc_raw).strip() == "":
            return "unknown", -1
        loc = str(loc_raw).strip().lower()
        try:
            codigo = int(self.loc_encoder.transform([loc])[0])
        except (ValueError, KeyError):
            codigo = -1
        return loc, codigo


