    def extrair_entidades(self, texto: str, secao_id: str) -> List[EntidadeNER]:
        """
        Extrai entidades médicas — VERSÃO OTIMIZADA (sem zero-shot por entidade).

        Camada 1: spaCy NER (entidades gerais) — rápido
        Camada 2: Descoberta por similaridade com sementes — rápido (batch embedding)
        Camada 3: Categorização pela semente mais próxima — instantâneo (argmax)

        ANTES: ~168s/seção (zero-shot por entidade)
        AGORA: ~1-3s/seção (similaridade vetorial)
        """
        entidades = []
        if not texto or len(texto.strip()) < 20:
            return entidades

        texto_proc = texto[:10000] if len(texto) > 10000 else texto

        # --- Camada 1: spaCy NER ---
        doc = self.nlp(texto_proc)

        candidatos_spacy = []
        for ent in doc.ents:
            ent_lower = ent.text.lower().strip()
            if len(ent_lower) >= 3 and ent_lower not in self.STOPWORDS_PT:
                candidatos_spacy.append((
                    ent_lower,
                    ent.start_char,
                    ent.end_char,
                    "spacy_ner"
                ))

        # --- Camada 2: Descoberta de termos por similaridade ---
        noun_phrases = set()
        for chunk in doc.noun_chunks:
            np_text = chunk.text.lower().strip()
            if (3 <= len(np_text) <= 60
                    and not np_text.isnumeric()
                    and np_text not in self.STOPWORDS_PT):
                noun_phrases.add(np_text)
        for token in doc:
            tok_lower = token.text.lower().strip()
            if (token.pos_ in ("NOUN", "PROPN")
                    and len(tok_lower) >= 4
                    and tok_lower not in self.STOPWORDS_PT):
                noun_phrases.add(tok_lower)

        # Juntar todos os candidatos (spaCy + noun phrases) para um único batch
        textos_spacy = [c[0] for c in candidatos_spacy]
        textos_np = list(noun_phrases - set(textos_spacy))  # Evitar duplicatas
        todos_textos = textos_spacy + textos_np

        if not todos_textos or self._sementes_embeddings is None:
            return entidades

        # --- Camada 3: Categorização por semente mais próxima (RÁPIDO) ---
        try:
            todos_embeddings = self.sentence_model.encode(
                todos_textos, show_progress_bar=False, batch_size=128
            )
            sims = cosine_similarity(todos_embeddings, self._sementes_embeddings)

            for i, texto_ent in enumerate(todos_textos):
                max_sim_idx = sims[i].argmax()
                max_sim_score = float(sims[i][max_sim_idx])

                # Threshold: entidades do spaCy têm threshold menor (já detectadas)
                is_spacy = i < len(textos_spacy)
                threshold = 0.30 if is_spacy else Config.NER_SIMILARITY_THRESHOLD

                if max_sim_score >= threshold:
                    # Categoria = categoria da semente mais similar
                    categoria = self._sementes_categorias[max_sim_idx]
                    metodo = "spacy_ner+seed" if is_spacy else "transformer_discovery"

                    # Posições originais (só disponíveis pra spaCy)
                    pos_inicio, pos_fim = -1, -1
                    if is_spacy:
                        pos_inicio = candidatos_spacy[i][1]
                        pos_fim = candidatos_spacy[i][2]

                    entidades.append(EntidadeNER(
                        texto=texto_ent,
                        categoria=categoria,
                        score_confianca=max_sim_score,
                        secao_id=secao_id,
                        posicao_inicio=pos_inicio,
                        posicao_fim=pos_fim,
                        metodo_deteccao=metodo
                    ))
        except Exception as e:
            logger.debug(f"Erro na extração de entidades: {e}")

        # Deduplicar por texto (manter maior score)
        melhores = {}
        for e in entidades:
            if e.texto not in melhores or e.score_confianca > melhores[e.texto].score_confianca:
                melhores[e.texto] = e

        return list(melhores.values())

    # ----- CLASSIFICAÇÃO DE SEÇÕES -----

    def classificar_secao(self, texto: str, secao_id: str) -> ClassificacaoSecao:
        """Classifica uma seção em categoria clínica usando zero-shot."""
        if not texto or len(texto.strip()) < 50:
            return ClassificacaoSecao(
                secao_id=secao_id,
                categoria_principal="indefinido",
                score_principal=0.0
            )

        # Usar primeiros 1000 caracteres (suficiente para classificação)
        texto_trunc = texto[:1000]
        try:
            resultado = self.classifier(
                texto_trunc,
                candidate_labels=self.CATEGORIAS_CLINICAS,
                multi_label=True
            )
            categorias_dict = {
                label: float(score)
                for label, score in zip(resultado["labels"], resultado["scores"])
            }
            return ClassificacaoSecao(
                secao_id=secao_id,
                categoria_principal=resultado["labels"][0],
                score_principal=float(resultado["scores"][0]),
                todas_categorias=categorias_dict
            )
        except Exception as e:
            logger.debug(f"Erro na classificação da seção {secao_id}: {e}")
            return ClassificacaoSecao(
                secao_id=secao_id,
                categoria_principal="erro",
                score_principal=0.0
            )

    # ----- EXTRAÇÃO DE RELAÇÕES -----

    def extrair_relacoes(self, texto: str, entidades: List[EntidadeNER],
                         secao_id: str) -> List[RelacaoEntidade]:
        """
        Extrai relações entre entidades co-ocorrentes na mesma sentença.
        Usa heurísticas de dependência + co-ocorrência.
        """
        relacoes = []
        if not texto or len(entidades) < 2:
            return relacoes

        doc = self.nlp(texto[:5000])
        entidade_set = {e.texto for e in entidades}
        entidade_cat = {e.texto: e.categoria for e in entidades}

        for sent in doc.sents:
            sent_text = sent.text.lower()
            # Encontrar entidades presentes nesta sentença
            presentes = [e for e in entidade_set if e in sent_text]

            if len(presentes) >= 2:
                # Gerar pares e inferir tipo de relação
                for i, e1 in enumerate(presentes):
                    for e2 in presentes[i+1:]:
                        cat1 = entidade_cat.get(e1, "")
                        cat2 = entidade_cat.get(e2, "")
                        tipo = self._inferir_relacao(cat1, cat2, sent.text)
                        if tipo:
                            relacoes.append(RelacaoEntidade(
                                entidade_origem=e1,
                                entidade_destino=e2,
                                tipo_relacao=tipo,
                                score_confianca=0.6,
                                secao_id=secao_id,
                                sentenca=sent.text[:200]
                            ))
        return relacoes

    def _inferir_relacao(self, cat1: str, cat2: str, sentenca: str) -> Optional[str]:
        """Infere tipo de relação baseado nas categorias das entidades."""
        par = frozenset([cat1, cat2])

        if par == frozenset(["DOENÇA", "SINTOMA"]):
            return "MANIFESTA"
        elif par == frozenset(["DOENÇA", "TRATAMENTO"]):
            return "TRATA"
        elif par == frozenset(["DOENÇA", "MEDICAMENTO"]):
            return "TRATA"
        elif par == frozenset(["DOENÇA", "LOCALIZAÇÃO"]):
            return "LOCALIZA_EM"
        elif par == frozenset(["DOENÇA", "EXAME"]):
            return "DIAGNOSTICA"
        elif par == frozenset(["DOENÇA", "PROCEDIMENTO"]):
            return "TRATA"
        elif "DOENÇA" in par:
            return "ASSOCIA_COM"
        return None

    # ----- EMBEDDINGS -----

    def gerar_embedding(self, texto: str) -> Optional[np.ndarray]:
        """Gera embedding de sentença usando Sentence-BERT."""
        if not texto or len(texto.strip()) < 10:
            return None
        try:
            # Truncar para limite do modelo
            texto_trunc = texto[:2000]
            return self.sentence_model.encode(texto_trunc, show_progress_bar=False)
        except Exception as e:
            logger.debug(f"Erro ao gerar embedding: {e}")
            return None

    def gerar_embeddings_batch(self, textos: List[str]) -> List[Optional[np.ndarray]]:
        """Gera embeddings em batch para eficiência."""
        resultados = []
        textos_validos = []
        indices_validos = []

        for i, t in enumerate(textos):
            if t and len(t.strip()) >= 10:
                textos_validos.append(t[:2000])
                indices_validos.append(i)

        if textos_validos:
            try:
                embeddings = self.sentence_model.encode(
                    textos_validos,
                    batch_size=Config.BATCH_SIZE,
                    show_progress_bar=True,
                    device=DEVICE
                )
                emb_map = {idx: emb for idx, emb in zip(indices_validos, embeddings)}
            except Exception as e:
                logger.error(f"Erro no batch de embeddings: {e}")
                emb_map = {}
        else:
            emb_map = {}

        for i in range(len(textos)):
            resultados.append(emb_map.get(i, None))

        return resultados


