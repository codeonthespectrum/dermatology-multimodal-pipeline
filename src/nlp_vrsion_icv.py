        termos_map = {}  # texto → id

        for e in tqdm(entidades, desc="Inserindo entidades"):
            # Tabela de entidades NER (NOVA)
            cursor.execute("""
                INSERT INTO entidades_ner
                (texto, categoria, score_confianca, secao_id,
                 posicao_inicio, posicao_fim, metodo_deteccao)
                VALUES (?, ?, ?, ?, ?, ?, ?)
            """, (e.texto, e.categoria, e.score_confianca, e.secao_id,
                  e.posicao_inicio, e.posicao_fim, e.metodo_deteccao))

            # Tabela de termos (compatível com RPA)
            if e.texto not in termos_map:
                cursor.execute("""
                    INSERT OR IGNORE INTO termos_dermatologicos
                    (termo, total_ocorrencias, categoria, score_confianca, metodo_deteccao)
                    VALUES (?, 1, ?, ?, ?)
                """, (e.texto, e.categoria, e.score_confianca, e.metodo_deteccao))
                cursor.execute("SELECT id FROM termos_dermatologicos WHERE termo = ?", (e.texto,))
                row = cursor.fetchone()
                if row:
                    termos_map[e.texto] = row[0]
            else:
                cursor.execute("""
                    UPDATE termos_dermatologicos
                    SET total_ocorrencias = total_ocorrencias + 1
                    WHERE termo = ?
                """, (e.texto,))

            # Relação seção ↔ termo
            if e.texto in termos_map:
                cursor.execute("""
                    INSERT OR IGNORE INTO relacao_secao_termo (secao_id, termo_id)
                    VALUES (?, ?)
                """, (e.secao_id, termos_map[e.texto]))

            self.estatisticas["registros_inseridos"]["entidades_ner"] += 1

