        print(f"\n📁 Banco: {Config.DB_PATH}")
        print(f"📁 Embeddings: {Config.EMBEDDINGS_DIR}")
        print(f"📁 Relatório: {Config.RELATORIO_PATH}")
        print("=" * 60)


