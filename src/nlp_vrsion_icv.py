class RoboAzulayTransformer:
    """
    Robô de extração do livro Azulay usando Transformers.

    DIFERENÇAS vs RPA:
    - RPA: regex para detectar capítulos/seções + busca de 99 termos fixos
    - Transformer: mesma detecção estrutural + NER neural + classificação
      zero-shot + embeddings + relações

    O que MANTÉM do RPA (para comparação justa):
    - Mesma lógica de detecção de capítulos/seções (estrutura do PDF)
    - Mesmo formato de saída (SecaoPDF, ImagemPDF)
    - Mesma extração de imagens

    O que MUDA:
    - Termos dermatológicos: descobertos pelo Transformer, não por lista fixa
    - Categorização: zero-shot, não por lookup de dicionário
    - ADICIONA: embeddings, relações, classificação de seções
    """

    # Padrões regex para estrutura do livro (IDÊNTICOS ao RPA)
    PADRAO_CAPITULO = re.compile(
        r"(?:Cap[ií]tulo|CAPÍTULO|CAPITULO)\s*(\d+)", re.IGNORECASE
    )
    PADRAO_SECAO = re.compile(
        r"^(?:\d+\.)+\s*[A-ZÁÉÍÓÚÂÊÔÃÕÇ]", re.MULTILINE
    )

    def __init__(self, pdf_path: str, output_dir: str, engine: TransformerNLPEngine):
        self.pdf_path = pdf_path
        self.output_dir = output_dir
        self.engine = engine
        self.doc = None
        self.secoes: List[SecaoPDF] = []
        self.imagens: List[ImagemPDF] = []
        # Resultados do Transformer (NOVOS)
        self.entidades: List[EntidadeNER] = []
        self.relacoes: List[RelacaoEntidade] = []
        self.classificacoes: List[ClassificacaoSecao] = []
        self.embeddings: List[EmbeddingSecao] = []
        self.estatisticas = {
            "total_paginas": 0,
            "total_secoes": 0,
            "total_imagens": 0,
            "total_capitulos": 0,
            "total_caracteres": 0,
            "total_palavras": 0,
            "total_entidades_ner": 0,
            "total_relacoes": 0,
            "total_embeddings": 0,
            "entidades_por_categoria": Counter(),
            "termos_encontrados": Counter(),
            "erros": []
        }

    def executar(self) -> Tuple[List[SecaoPDF], List[ImagemPDF], dict]:
        """Pipeline principal do Robô 1."""
        logger.info("📖 Abrindo PDF do Azulay...")
        self.doc = fitz.open(self.pdf_path)
        self.estatisticas["total_paginas"] = len(self.doc)
        logger.info(f"   Total de páginas: {len(self.doc)}")

        # Etapa 1: Extração bruta de texto por página (IDÊNTICA ao RPA)
        logger.info("📝 Etapa 1/7: Extraindo texto bruto...")
        texto_por_pagina = self._extrair_texto_bruto()

        # Etapa 2: Identificação de estrutura hierárquica (IDÊNTICA ao RPA)
        logger.info("🏗️  Etapa 2/7: Identificando estrutura do livro...")
        self._identificar_estrutura(texto_por_pagina)

        # Etapa 3: Extração de imagens (IDÊNTICA ao RPA)
        logger.info("🖼️  Etapa 3/7: Extraindo imagens...")
        self._extrair_imagens()

        # Etapa 4: NER com Transformer (NOVO)
        logger.info("🔬 Etapa 4/7: Executando NER com Transformers...")
        self._executar_ner()

        # Etapa 5: Classificação de seções (NOVO)
        logger.info("🏷️  Etapa 5/7: Classificando seções por categoria clínica...")
        self._classificar_secoes()

        # Etapa 6: Extração de relações (NOVO)
        logger.info("🔗 Etapa 6/7: Extraindo relações entre entidades...")
        self._extrair_relacoes()

        # Etapa 7: Geração de embeddings (NOVO)
        logger.info("📐 Etapa 7/7: Gerando embeddings de seções...")
        self._gerar_embeddings()

        self.doc.close()

        logger.info(f"✅ Robô 1 concluído!")
        logger.info(f"   Seções: {len(self.secoes)}")
        logger.info(f"   Imagens: {len(self.imagens)}")
        logger.info(f"   Entidades NER: {len(self.entidades)}")
        logger.info(f"   Relações: {len(self.relacoes)}")
        logger.info(f"   Embeddings: {len(self.embeddings)}")

        return self.secoes, self.imagens, self.estatisticas

    def _extrair_texto_bruto(self) -> Dict[int, str]:
        """Extrai texto de cada página do PDF."""
        texto_por_pagina = {}
        for i in tqdm(range(len(self.doc)), desc="Extraindo texto"):
            try:
                page = self.doc[i]
                texto = page.get_text("text")
                if texto.strip():
                    texto_por_pagina[i] = texto
                    self.estatisticas["total_caracteres"] += len(texto)
                    self.estatisticas["total_palavras"] += len(texto.split())
            except Exception as e:
                self.estatisticas["erros"].append(f"Página {i}: {e}")
        return texto_por_pagina

    def _identificar_estrutura(self, texto_por_pagina: Dict[int, str]):
        """Identifica capítulos e seções (mesma lógica do RPA)."""
        capitulo_atual = "Introdução"
        num_capitulo_atual = 0
        secao_atual = ""
        texto_acumulado = ""
        pagina_inicio = 0

        def _salvar_secao():
            nonlocal texto_acumulado
            if texto_acumulado.strip():
                secao_id = f"s{len(self.secoes)+1:04d}"
                palavras = texto_acumulado.split()
                self.secoes.append(SecaoPDF(
                    id=secao_id,
                    capitulo=capitulo_atual,
                    numero_capitulo=num_capitulo_atual,
                    secao=secao_atual or capitulo_atual,
                    subsecao="",
                    texto=texto_acumulado.strip(),
                    pagina_inicio=pagina_inicio,
                    pagina_fim=pagina_atual,
                    nivel_hierarquia=2 if secao_atual else 1,
                    palavras_chave=[],   # Será preenchido pelo NER
                    doencas_mencionadas=[]  # Será preenchido pelo NER
                ))
                texto_acumulado = ""

        for pagina_atual, texto in sorted(texto_por_pagina.items()):
            linhas = texto.split("\n")
            for linha in linhas:
                linha_limpa = linha.strip()
                if not linha_limpa:
                    continue

                # Detectar capítulo
                match_cap = self.PADRAO_CAPITULO.search(linha_limpa)
                if match_cap:
                    _salvar_secao()
                    num_capitulo_atual = int(match_cap.group(1))
                    capitulo_atual = linha_limpa
                    secao_atual = ""
                    pagina_inicio = pagina_atual
                    self.estatisticas["total_capitulos"] = max(
                        self.estatisticas["total_capitulos"], num_capitulo_atual
                    )
                    continue

                # Detectar seção
                if self.PADRAO_SECAO.match(linha_limpa) and len(linha_limpa) < 200:
                    _salvar_secao()
                    secao_atual = linha_limpa
                    pagina_inicio = pagina_atual
                    continue

                texto_acumulado += linha_limpa + " "

        # Salvar última seção
        _salvar_secao()
        self.estatisticas["total_secoes"] = len(self.secoes)

    def _extrair_imagens(self):
        """Extrai imagens do PDF (mesma lógica do RPA)."""
        img_dir = os.path.join(self.output_dir, "azulay")
        os.makedirs(img_dir, exist_ok=True)

        for page_idx in tqdm(range(len(self.doc)), desc="Extraindo imagens"):
            try:
                page = self.doc[page_idx]
                imagens = page.get_images(full=True)

                for img_idx, img_info in enumerate(imagens):
                    xref = img_info[0]
                    try:
                        base_image = self.doc.extract_image(xref)
                        if base_image:
                            img_bytes = base_image["image"]
                            fmt = base_image.get("ext", "png")
                            w = base_image.get("width", 0)
                            h = base_image.get("height", 0)

                            # Filtrar imagens muito pequenas
                            if w < 50 or h < 50:
                                continue

                            hash_md5 = hashlib.md5(img_bytes).hexdigest()
                            nome = f"azulay_p{page_idx:04d}_i{img_idx:02d}.{fmt}"
                            caminho = os.path.join(img_dir, nome)

                            with open(caminho, "wb") as f:
                                f.write(img_bytes)

                            # Tentar extrair legenda
                            legenda = self._extrair_legenda(page, page_idx)

                            self.imagens.append(ImagemPDF(
                                id=f"img_az_{page_idx:04d}_{img_idx:02d}",
                                pagina=page_idx,
                                capitulo=self._capitulo_da_pagina(page_idx),
                                legenda=legenda,
                                caminho_arquivo=caminho,
                                largura=w,
                                altura=h,
                                formato=fmt,
                                hash_md5=hash_md5
                            ))
                    except Exception:
                        pass
            except Exception as e:
                self.estatisticas["erros"].append(f"Imagem página {page_idx}: {e}")

        self.estatisticas["total_imagens"] = len(self.imagens)

    def _extrair_legenda(self, page, page_idx) -> str:
        """Tenta extrair legenda de figura da página."""
        texto = page.get_text("text")
        padroes = [
            re.compile(r"(?:Figura|Fig\.?|FIGURA)\s*\d+[.:]\s*(.+?)(?:\n|$)", re.IGNORECASE),
        ]
        for p in padroes:
            match = p.search(texto)
            if match:
                return match.group(1).strip()[:300]
        return ""

    def _capitulo_da_pagina(self, pagina: int) -> str:
        """Retorna o capítulo correspondente a uma página."""
        cap = "Introdução"
        for s in self.secoes:
            if s.pagina_inicio <= pagina:
                cap = s.capitulo
        return cap

    # --- ETAPAS TRANSFORMER (NOVAS) ---

    def _executar_ner(self):
        """Executa NER com Transformers em todas as seções."""
        for secao in tqdm(self.secoes, desc="NER Transformer"):
            try:
                entidades = self.engine.extrair_entidades(secao.texto, secao.id)
                self.entidades.extend(entidades)

                # Preencher palavras_chave e doencas_mencionadas da seção
                secao.palavras_chave = [
                    e.texto for e in entidades if e.score_confianca > 0.4
                ][:20]
                secao.doencas_mencionadas = [
                    e.texto for e in entidades if e.categoria == "DOENÇA"
                ]

                # Atualizar estatísticas
                for e in entidades:
                    self.estatisticas["entidades_por_categoria"][e.categoria] += 1
                    self.estatisticas["termos_encontrados"][e.texto] += 1

            except Exception as e:
                self.estatisticas["erros"].append(f"NER seção {secao.id}: {e}")

        self.estatisticas["total_entidades_ner"] = len(self.entidades)

    def _classificar_secoes(self):
        """Classifica cada seção em categoria clínica."""
        for secao in tqdm(self.secoes, desc="Classificando seções"):
            try:
                classificacao = self.engine.classificar_secao(secao.texto, secao.id)
                self.classificacoes.append(classificacao)
            except Exception as e:
                self.estatisticas["erros"].append(f"Classificação seção {secao.id}: {e}")

    def _extrair_relacoes(self):
        """Extrai relações entre entidades co-ocorrentes."""
        entidades_por_secao = defaultdict(list)
        for e in self.entidades:
            entidades_por_secao[e.secao_id].append(e)

        for secao in tqdm(self.secoes, desc="Extraindo relações"):
            ents = entidades_por_secao.get(secao.id, [])
            if len(ents) >= 2:
                try:
                    rels = self.engine.extrair_relacoes(secao.texto, ents, secao.id)
                    self.relacoes.extend(rels)
                except Exception as e:
                    self.estatisticas["erros"].append(f"Relações seção {secao.id}: {e}")

        self.estatisticas["total_relacoes"] = len(self.relacoes)

    def _gerar_embeddings(self):
        """Gera embeddings para todas as seções em batch."""
        textos = [s.texto for s in self.secoes]
        embeddings = self.engine.gerar_embeddings_batch(textos)

        for secao, emb in zip(self.secoes, embeddings):
            if emb is not None:
                self.embeddings.append(EmbeddingSecao(
                    secao_id=secao.id,
                    vetor=emb.tolist(),
                    modelo=Config.SENTENCE_MODEL,
                    dimensao=len(emb)
                ))

        self.estatisticas["total_embeddings"] = len(self.embeddings)


