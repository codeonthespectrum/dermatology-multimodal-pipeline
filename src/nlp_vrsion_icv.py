# 2. TOP 30 TERMOS MAIS FREQUENTES (Transformer vs RPA)
# ============================================================
cursor.execute("""
    SELECT termo, total_ocorrencias, categoria
    FROM termos_dermatologicos
    ORDER BY total_ocorrencias DESC LIMIT 30
""")
rows = cursor.fetchall()
termos = [r[0][:25] for r in rows]
freq = [r[1] for r in rows]
cat_termos = [r[2] for r in rows]

# Mapa de cores por categoria
cat_colors = {
    "DOENÇA": "#e74c3c", "SINTOMA": "#e67e22", "LESÃO_ELEMENTAR": "#f1c40f",
    "NEOPLASIA": "#c0392b", "INFECÇÃO": "#9b59b6", "ESTRUTURA": "#3498db",
    "PROCEDIMENTO": "#2ecc71", "MEDICAMENTO": "#1abc9c", "MORFOLOGIA": "#95a5a6",
    "EXAME": "#34495e", "PATÓGENO": "#d35400", "OUTRO": "#bdc3c7"
}
bar_colors = [cat_colors.get(c, "#bdc3c7") for c in cat_termos]

fig, ax = plt.subplots(figsize=(14, 10))
bars = ax.barh(termos[::-1], freq[::-1], color=bar_colors[::-1], edgecolor='white')
for bar, val in zip(bars, freq[::-1]):
    ax.text(bar.get_width() + 10, bar.get_y() + bar.get_height()/2,
            f'{val:,}', va='center', fontsize=8, fontweight='bold')
ax.set_xlabel('Ocorrências no Texto')
ax.set_title('Top 30 Termos Dermatológicos — NLP Transformer\n(cores = categoria NER)',
             fontsize=13, fontweight='bold')
ax.spines['top'].set_visible(False)
ax.spines['right'].set_visible(False)

# Legenda
from matplotlib.patches import Patch
legend_cats = list(set(cat_termos))
legend_elements = [Patch(facecolor=cat_colors.get(c, "#bdc3c7"), label=c)
                   for c in sorted(legend_cats)]
ax.legend(handles=legend_elements, loc='lower right', fontsize=8, ncol=2)

plt.tight_layout()
plt.savefig(os.path.join(Config.OUTPUT_DIR, "02_top30_termos.png"),
            bbox_inches='tight', facecolor='white')
plt.show()
print("✅ 2/8 — Top 30 termos")


