# 1. DISTRIBUIÇÃO DE ENTIDADES NER POR CATEGORIA
# ============================================================
cursor.execute("""
    SELECT categoria, COUNT(*), AVG(score_confianca)
    FROM entidades_ner
    GROUP BY categoria ORDER BY COUNT(*) DESC
""")
rows = cursor.fetchall()
cats = [r[0] for r in rows]
counts = [r[1] for r in rows]
scores = [r[2] for r in rows]

fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(16, 6))

colors_ner = plt.cm.Set3(np.linspace(0, 1, len(cats)))
bars = ax1.barh(cats[::-1], counts[::-1], color=colors_ner)
for bar, val in zip(bars, counts[::-1]):
    ax1.text(bar.get_width() + 200, bar.get_y() + bar.get_height()/2,
             f'{val:,}', va='center', fontsize=9, fontweight='bold')
ax1.set_xlabel('Quantidade de Entidades')
ax1.set_title('Entidades NER por Categoria', fontsize=13, fontweight='bold')
ax1.spines['top'].set_visible(False)
ax1.spines['right'].set_visible(False)

ax2.barh(cats[::-1], scores[::-1], color=colors_ner)
for i, (s, c) in enumerate(zip(scores[::-1], cats[::-1])):
    ax2.text(s + 0.005, i, f'{s:.3f}', va='center', fontsize=9)
ax2.set_xlabel('Score Médio de Confiança')
ax2.set_title('Confiança Média por Categoria', fontsize=13, fontweight='bold')
ax2.set_xlim(0, 1)
ax2.spines['top'].set_visible(False)
ax2.spines['right'].set_visible(False)

plt.tight_layout()
plt.savefig(os.path.join(Config.OUTPUT_DIR, "01_entidades_por_categoria.png"),
            bbox_inches='tight', facecolor='white')
plt.show()
print("✅ 1/8 — Entidades por categoria")


