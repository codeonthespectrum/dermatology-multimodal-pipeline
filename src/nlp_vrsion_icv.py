class RoboValidacaoTransformer:
    """
    Validação do banco de dados + métricas específicas do Transformer.

    Verificações idênticas ao RPA:
    - Contagem de registros, integridade, distribuição

    Verificações NOVAS:
    - Estatísticas de NER (entidades por categoria, cobertura)
    - Distribuição de classificações de seções
    - Cobertura de embeddings
    - Métricas de variáveis clínicas
    """

    def __init__(self, db_path: str):
        self.db_path = db_path
        self.relatorio = {}

    def executar(self, stats_azulay, stats_ham, stats_derm7pt, stats_db) -> dict:
        conn = sqlite3.connect(self.db_path)
        cursor = conn.cursor()

        # 1. Contagem de registros
        logger.info("📊 Verificando contagem de registros...")
        contagens = {}
        tabelas = [
            "capitulos", "secoes_texto", "imagens_azulay",
            "imagens_ham10000", "imagens_derm7pt",
            "diagnosticos", "termos_dermatologicos",
            "entidades_ner", "relacoes_entidades",
            "classificacoes_secao", "embeddings_secao"
        ]
        for t in tabelas:
            try:
                cursor.execute(f"SELECT COUNT(*) FROM {t}")
                contagens[t] = cursor.fetchone()[0]
            except:
                contagens[t] = 0

        # 2. Distribuição de entidades NER
        logger.info("🔬 Estatísticas de NER...")
        dist_ner = {}
        try:
            cursor.execute("""
                SELECT categoria, COUNT(*), AVG(score_confianca)
                FROM entidades_ner
                GROUP BY categoria ORDER BY COUNT(*) DESC
            """)
            dist_ner = {
                row[0]: {"total": row[1], "score_medio": round(row[2], 3)}
                for row in cursor.fetchall()
            }
        except:
            pass

        # 3. Top termos
        logger.info("🔬 Top termos...")
        top_termos = []
        try:
            cursor.execute("""
                SELECT termo, total_ocorrencias, categoria, score_confianca
                FROM termos_dermatologicos
                ORDER BY total_ocorrencias DESC LIMIT 30
            """)
            top_termos = [
                {"termo": r[0], "ocorrencias": r[1], "categoria": r[2], "score": r[3]}
                for r in cursor.fetchall()
            ]
        except:
            pass

        # 4. Distribuição de diagnósticos
        logger.info("📈 Distribuição de diagnósticos...")
        dist_ham = {}
        try:
            cursor.execute("""
                SELECT diagnostico_codigo, diagnostico_nome, COUNT(*)
                FROM imagens_ham10000 GROUP BY diagnostico_codigo ORDER BY COUNT(*) DESC
            """)
            dist_ham = {r[0]: {"nome": r[1], "total": r[2]} for r in cursor.fetchall()}
        except:
            pass

        dist_derm7pt = {}
        try:
            cursor.execute("""
                SELECT diagnostico_original, diagnostico_ptbr, COUNT(*)
                FROM imagens_derm7pt GROUP BY diagnostico_original ORDER BY COUNT(*) DESC
            """)
            dist_derm7pt = {r[0]: {"nome": r[1], "total": r[2]} for r in cursor.fetchall()}
        except:
            pass

        # 5. Distribuição de classificações de seções
        dist_classificacoes = {}
        try:
            cursor.execute("""
                SELECT categoria_principal, COUNT(*), AVG(score_principal)
                FROM classificacoes_secao GROUP BY categoria_principal ORDER BY COUNT(*) DESC
            """)
            dist_classificacoes = {
                r[0]: {"total": r[1], "score_medio": round(r[2], 3)}
                for r in cursor.fetchall()
            }
        except:
            pass

        # 6. Variáveis clínicas
        logger.info("📊 Variáveis clínicas...")
        stats_clinicas = {}
        try:
            cursor.execute("""
                SELECT AVG(idade), MIN(idade), MAX(idade),
                       COUNT(CASE WHEN sexo='male' THEN 1 END),
                       COUNT(CASE WHEN sexo='female' THEN 1 END),
                       COUNT(CASE WHEN sexo='unknown' THEN 1 END),
                       COUNT(DISTINCT localizacao)
                FROM imagens_ham10000
            """)
            r = cursor.fetchone()
            if r:
                stats_clinicas["ham10000"] = {
                    "idade_media": round(r[0], 1) if r[0] else None,
                    "idade_min": r[1], "idade_max": r[2],
                    "sexo_male": r[3], "sexo_female": r[4], "sexo_unknown": r[5],
                    "localizacoes_unicas": r[6]
                }
        except:
            pass

        # 7. Relações
        dist_relacoes = {}
        try:
            cursor.execute("""
                SELECT tipo_relacao, COUNT(*) FROM relacoes_entidades
                GROUP BY tipo_relacao ORDER BY COUNT(*) DESC
            """)
            dist_relacoes = {r[0]: r[1] for r in cursor.fetchall()}
        except:
            pass

        conn.close()

        # Montar relatório
        self.relatorio = {
            "data_execucao": datetime.now().isoformat(),
            "pipeline": "NLP Transformer v2.0",
            "modelos": {
                "bert": Config.BERT_MODEL,
                "sentence": Config.SENTENCE_MODEL,
                "zero_shot": Config.ZERO_SHOT_MODEL
            },
            "resumo": {
                "total_registros_banco": sum(contagens.values()),
                "contagens_por_tabela": contagens
            },
            "azulay": {
                "paginas_processadas": stats_azulay.get("total_paginas", 0),
                "capitulos_detectados": stats_azulay.get("total_capitulos", 0),
                "secoes_extraidas": contagens.get("secoes_texto", 0),
                "imagens_extraidas": contagens.get("imagens_azulay", 0),
                "entidades_ner_total": contagens.get("entidades_ner", 0),
                "entidades_por_categoria": dist_ner,
                "relacoes_total": contagens.get("relacoes_entidades", 0),
                "relacoes_por_tipo": dist_relacoes,
                "classificacoes_secao": dist_classificacoes,
                "embeddings_gerados": contagens.get("embeddings_secao", 0),
                "top_termos": top_termos
            },
            "ham10000": {
                "total_processado": contagens.get("imagens_ham10000", 0),
                "distribuicao_diagnosticos": dist_ham,
                "variaveis_clinicas": stats_clinicas.get("ham10000", {})
            },
            "derm7pt": {
                "total_processado": contagens.get("imagens_derm7pt", 0),
                "distribuicao_diagnosticos": dist_derm7pt
            },
            "diagnosticos_unificados": contagens.get("diagnosticos", 0),
            "qualidade": {
                "score_completude": self._calcular_score(contagens),
            }
        }

        # Salvar relatório
        with open(Config.RELATORIO_PATH, "w", encoding="utf-8") as f:
            json.dump(self.relatorio, f, ensure_ascii=False, indent=2)

        self._imprimir_resumo()
        return self.relatorio

    def _calcular_score(self, contagens):
        score = 0
        if contagens.get("secoes_texto", 0) > 0: score += 15
        if contagens.get("imagens_azulay", 0) > 0: score += 5
        if contagens.get("imagens_ham10000", 0) > 0: score += 15
        if contagens.get("imagens_derm7pt", 0) > 0: score += 15
        if contagens.get("termos_dermatologicos", 0) > 0: score += 10
        if contagens.get("entidades_ner", 0) > 0: score += 15
        if contagens.get("relacoes_entidades", 0) > 0: score += 10
        if contagens.get("classificacoes_secao", 0) > 0: score += 5
        if contagens.get("embeddings_secao", 0) > 0: score += 10
        return score

    def _imprimir_resumo(self):
        r = self.relatorio
        print("\n" + "=" * 60)
        print("📋 RELATÓRIO FINAL — NLP TRANSFORMER PIPELINE")
        print("=" * 60)
        print(f"\n📅 Data: {r['data_execucao']}")
        print(f"\n🧠 MODELOS UTILIZADOS:")
        for k, v in r["modelos"].items():
            print(f"   {k}: {v}")

        print(f"\n📊 BANCO DE DADOS:")
        for t, c in r["resumo"]["contagens_por_tabela"].items():
            print(f"   • {t}: {c:,} registros")
        print(f"\n   TOTAL: {r['resumo']['total_registros_banco']:,}")

        print(f"\n📖 AZULAY (Transformer):")
        az = r["azulay"]
        print(f"   Seções: {az['secoes_extraidas']}")
        print(f"   Imagens: {az['imagens_extraidas']}")
        print(f"   Entidades NER: {az['entidades_ner_total']}")
        if az["entidades_por_categoria"]:
            for cat, info in az["entidades_por_categoria"].items():
                print(f"     → {cat}: {info['total']} (score médio: {info['score_medio']})")
        print(f"   Relações: {az['relacoes_total']}")
        print(f"   Embeddings: {az['embeddings_gerados']}")

        print(f"\n🖼️  HAM10000: {r['ham10000']['total_processado']:,} imagens")
        vc = r["ham10000"].get("variaveis_clinicas", {})
        if vc:
            print(f"   Idade média: {vc.get('idade_media')}")
            print(f"   Sexo: M={vc.get('sexo_male')}, F={vc.get('sexo_female')}, ?={vc.get('sexo_unknown')}")
            print(f"   Localizações: {vc.get('localizacoes_unicas')} únicas")

        print(f"\n🖼️  derm7pt: {r['derm7pt']['total_processado']:,} imagens")
        print(f"🏷️  Diagnósticos únicos: {r['diagnosticos_unificados']}")

